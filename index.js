const baseURL = "https://leagueoflegends.fandom.com/wiki";

const downloadAudio = async (url, filename) => {
  const response = await axios({
    method: "GET",
    url: url,
    responseType: "stream",
  });

  response.data.pipe(fs.createWriteStream(`downloads/${filename}`));
};

const fetchAndParsePage = async (url, championName) => {
  try {
    const response = await axios.get(url);
    const $ = cheerio.load(response.data);

    $("audio source").each((index, element) => {
      const audioUrl = $(element).attr("src");
      const filename = `${championName}_${path.basename(audioUrl)}.ogg`;
      console.log(`=== Downloading ${filename}...`);

      downloadAudio(audioUrl, filename);
    });
  } catch (error) {
    console.error(
      `Error fetching or parsing the page for ${championName}:`,
      error
    );
  }
};

const main = async () => {
  for (const champion of champions) {
    console.log(`Fetching ${champion}...`);
    const championUrl = `${baseURL}/${champion}/LoL/Audio`;
    await fetchAndParsePage(championUrl, champion);
  }
};

main();
